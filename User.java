public class User {
    private int idUser;
    private String nama;
    private String username;
    private String password;
    private String alamat;
    private int idRole;

    // Constructor, Getter, dan Setter
    public User(int idUser, String nama, String username, String password, String alamat, int idRole) {
        this.idUser = idUser;
        this.nama = nama;
        this.username = username;
        this.password = password;
        this.alamat = alamat;
        this.idRole = idRole;
    }
    
    // Getter & Setter di sini...
}